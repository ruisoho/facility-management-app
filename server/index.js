const express = require('express');
const mongoose = require('mongoose');
const cors = require('cors');
const helmet = require('helmet');
const compression = require('compression');
const rateLimit = require('express-rate-limit');
require('dotenv').config();

const app = express();
const PORT = process.env.PORT || 5000;

// Security middleware
app.use(helmet());
app.use(compression());

// Rate limiting
const limiter = rateLimit({
  windowMs: 15 * 60 * 1000, // 15 minutes
  max: 100 // limit each IP to 100 requests per windowMs
});
app.use(limiter);

// CORS configuration
app.use(cors({
  origin: 'http://localhost:3000',
  credentials: true
}));

// Body parsing middleware
app.use(express.json({ limit: '10mb' }));
app.use(express.urlencoded({ extended: true, limit: '10mb' }));

// Static files for uploaded documents
app.use('/uploads', express.static('uploads'));

// MongoDB connection with better error handling
const MONGODB_URI = process.env.MONGODB_URI || 'mongodb://localhost:27017/facility_management';

// Try to connect to MongoDB, but don't fail if it's not available
let mongoConnected = false;

mongoose.connect(MONGODB_URI, {
  useNewUrlParser: true,
  useUnifiedTopology: true,
  serverSelectionTimeoutMS: 5000, // Timeout after 5s instead of 30s
  socketTimeoutMS: 45000, // Close sockets after 45s of inactivity
})
.then(() => {
  console.log('âœ… Connected to MongoDB');
  console.log(`ðŸ“Š Database: ${MONGODB_URI}`);
  mongoConnected = true;
})
.catch(err => {
  console.error('âŒ MongoDB connection error:', err.message);
  console.log('ðŸ’¡ Running in development mode without MongoDB');
  console.log('ðŸ’¡ Some features may not work properly');
  mongoConnected = false;
});

// Handle MongoDB connection events
mongoose.connection.on('error', (err) => {
  console.error('âŒ MongoDB connection error:', err);
});

mongoose.connection.on('disconnected', () => {
  console.log('âš ï¸ MongoDB disconnected');
});

mongoose.connection.on('reconnected', () => {
  console.log('âœ… MongoDB reconnected');
});

// Import routes
const maintenanceRoutes = require('./routes/maintenance');
const consumptionRoutes = require('./routes/consumption');
const taskRoutes = require('./routes/tasks');
const uploadRoutes = require('./routes/upload');

// API routes
app.use('/api/maintenance', maintenanceRoutes);
app.use('/api/consumption', consumptionRoutes);
app.use('/api/tasks', taskRoutes);
app.use('/api/upload', uploadRoutes);

// Health check endpoint
app.get('/api/health', (req, res) => {
  res.json({ 
    status: 'OK', 
    timestamp: new Date().toISOString(),
    environment: process.env.NODE_ENV || 'development'
  });
});

// Error handling middleware
app.use((err, req, res, next) => {
  console.error('Error:', err.stack);
  res.status(500).json({ 
    message: 'Something went wrong!',
    error: process.env.NODE_ENV === 'development' ? err.message : 'Internal server error'
  });
});

// 404 handler
app.use('*', (req, res) => {
  res.status(404).json({ message: 'Route not found' });
});

app.listen(PORT, () => {
  console.log(`ðŸš€ Server running on port ${PORT}`);
  console.log(`ðŸ“Š Environment: ${process.env.NODE_ENV || 'development'}`);
  console.log(`ðŸ”— API Base URL: http://localhost:${PORT}/api`);
});